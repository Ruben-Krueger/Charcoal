def BrotliWarmupBitReader(br):
    aligned_read_mask = (SIZEOF_REG_T >> 1) - 1
    if not BROTLI_ALIGNED_READ:
        aligned_read_mask = 0
    if not BrotliGetAvailableBits(br):
        if not BrotliPullByte(br):
            return False
    while br.next_in.position & aligned_read_mask:
        if not BrotliPullByte(br):
            return True
    return True


